CREATE TABLE KHACHHANG
(
	MAKH INT, 
	HOTEN NVARCHAR(50), 
	CCCD CHAR(12),
	SDT CHAR(11), 
	DIACHI NVARCHAR(60), 
	FAXID INT, 
	EMAIL VARCHAR(50),
	CONSTRAINT PK_KHACHHANG PRIMARY KEY(MAKH)
)

CREATE TABLE TAIKHOAN
(
	USERNAME VARCHAR(30),
	PASS VARCHAR(30) NOT NULL,
	LOAI TINYINT NOT NULL,
	CONSTRAINT PK_TAIKHOAN PRIMARY KEY(USERNAME)
)
CREATE TABLE NHANVIEN
(
	MANV INT, 
	HOTEN NVARCHAR(50), 
	SDT CHAR(11), 
	DIACHI NVARCHAR(60), 
	EMAIL VARCHAR(50),
	USERNAME VARCHAR(30),
	CONSTRAINT PK_NHANVIEN PRIMARY KEY(MANV),
	FOREIGN KEY(USERNAME) REFERENCES TAIKHOAN(USERNAME)
)
CREATE TABLE PHONG
(
	MAPHONG INT, 
	LOAIPHONG NVARCHAR(8), -- Đơn / Đôi
	HANG NVARCHAR(8), -- Thường / Cao cấp
	GIA INT,
	TINHTRANG NVARCHAR(15), -- Trống / Chưa dọn / Đang dùng
	CONSTRAINT PK_PHONG PRIMARY KEY(MAPHONG)
)
CREATE TABLE PHIEUDATPHONG
(
	MAPDP INT, 
	NGAYLAP DATETIME,
	NGAYNHANPHONG DATE,
	NGAYTRAPHONG DATE,
	MAKH INT,
	MANV INT,
	CONSTRAINT PK_PDP PRIMARY KEY(MAPDP),
	FOREIGN KEY(MAKH) REFERENCES KHACHHANG(MAKH)
)
CREATE TABLE CHITIETPHIEUDATPHONG
(
	MAPDP INT, 
	MAPHONG INT,
	CONSTRAINT PK_CTPDP PRIMARY KEY(MAPDP, MAPHONG),
	FOREIGN KEY(MAPHONG) REFERENCES PHONG(MAPHONG)
)

CREATE TABLE HOADON
(
	MAHD INT, 
	NGAYLAP DATETIME,
	MAPDP INT, 
	TONGTIEN INT default 0,
	TINHTRANGTT NVARCHAR(16), --Đã thanh toán / Đã cọc
	MANV INT, 
	CONSTRAINT PK_HOADON PRIMARY KEY(MAHD),
	FOREIGN KEY(MAPDP) REFERENCES PHIEUDATPHONG(MAPDP),
	FOREIGN KEY(MANV) REFERENCES NHANVIEN(MANV)
)
CREATE TABLE DICHVU
(
	MADV INT, 
	TENDV NVARCHAR(30),
	GIA INT,
	MOTA NVARCHAR(100),
	DIADIEM NVARCHAR(30),
	CONSTRAINT PK_DV PRIMARY KEY(MADV)
)
CREATE TABLE PDT_DICHVU
(
	MADV INT, 
	MAPDP INT, 
	SOLUONG INT, 
	THOIGIAN DATETIME,
	CONSTRAINT PK_PDT_DV PRIMARY KEY(MADV, MAPDP),
	FOREIGN KEY(MADV) REFERENCES DICHVU(MADV),
	FOREIGN KEY(MAPDP) REFERENCES PHIEUDATPHONG(MAPDP)
)


 create table DOITAC
 (
 	MADOITAC int, 
 	TenDoiTac nvarchar(50), 
 	SDT int
 	Constraint PK_Doitac Primary key(MaDoiTac)
 )	

 create table TOUR
 (
 	MATOUR int, 
 	TENTOUR NVARCHAR(50), 
 	DIADIEM NVARCHAR(50),
 	GIA int,
 	MOTACT NVARCHAR(100),
 	MADOITAC int
 	Constraint PK_TOUR Primary key(MATOUR),
 	FOREIGN KEY(MADOITAC) REFERENCES DOITAC(MADOITAC)
 )
 create table PHIEUDANGKYTOUR
 (
	MAPDKTOUR int,
 	THGIANKHOIHANH DATETIME,
 	SONGUOITHGIA INT,
 	DICHVUDUADON NVARCHAR(20),--???????Tự túc/Xe đưa đón
 	YEUCAUDACBIET nvarchar(100), 
