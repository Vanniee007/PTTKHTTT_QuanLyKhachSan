	MATOUR int,
 	Constraint PK_PDKTOUR Primary key(MAKH,MATOUR),
	FOREIGN KEY(MAKH) REFERENCES KHACHHANG(MAKH),
	FOREIGN KEY(MATOUR) REFERENCES TOUR(MATOUR)
